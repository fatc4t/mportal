        /**
         * 帳票フォーム一覧取得SQL文作成
         * @param    $postArray                 入力パラメータ(is_del/ledger_sheet_form_id)
         * @param    $searchArray               検索条件用パラメータ
         * @return   帳票フォーム一覧取得SQL文
         */
        private function creatSQL( $postArray, &$searchArray )
        {
            global $Log; // グローバル変数宣言
            $Log->trace("START creatSQL");

            $where = '';
            if( !empty( $postArray['start_date'] ) )
            {
                $where .= ' WHERE date(sale_str_dt1) >= :start_date ';
                $ledgerSheetFormIdArray = array(':start_date' => $postArray['start_date'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }
           
            if( !empty( $postArray['end_date'] ) )
            {
                $where .= ' AND date(sale_str_dt1) <= :end_date ';
                $ledgerSheetFormIdArray = array(':end_date' => $postArray['end_date'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }
            
            if( !empty( $postArray['organization_id'] ) )
            {
                $where .= ' AND a.organization_id = :organization_id ';
                $ledgerSheetFormIdArray = array(':organization_id' => $postArray['organization_id'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }

            if( !empty( $postArray['spc_start'] ) )
            {
                $where .= ' AND a.sale_plan_cd >= :spc_start ';
                $ledgerSheetFormIdArray = array(':spc_start' => $postArray['spc_start'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }

            if( !empty( $postArray['spc_end'] ) )
            {
                $where .= ' AND a.sale_plan_cd <= :spc_end ';
                $ledgerSheetFormIdArray = array(':spc_end' => $postArray['spc_end'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }
            if( !empty( $postArray['prod_k'] ) )
            {
                $where .= ' AND a.prod_cd >= :prod_k';
                $ledgerSheetFormIdArray = array(':prod_k' => $postArray['prod_k'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }
            if( !empty( $postArray['prod_s'] ) )
            {
                $where .= ' AND a.prod_cd <= :prod_s';
                $ledgerSheetFormIdArray = array(':prod_s' => $postArray['prod_s'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }
            if( !empty( $postArray['prod_nm'] ))
            {
                $where .= ' AND z.prod_nm ~ :prod_nm';
                $ledgerSheetFormIdArray = array(':prod_nm' => $postArray['prod_nm'],);
                $searchArray = array_merge($searchArray, $ledgerSheetFormIdArray);
            }

            $sql  = " SELECT 
                   a.sale_plan_cd AS sale_plan_cd,
                   b.sale_plan_nm AS sale_plan_nm, 
                   to_char(date(a.sale_str_dt1), 'YYYY/MM/DD') AS sale_str_dt1, 
                   to_char(date(a.sale_end_dt1), 'YYYY/MM/DD') AS sale_end_dt1,
                   to_char(date(a.sale_str_dt2), 'YYYY/MM/DD') AS sale_str_dt2, 
                   to_char(date(a.sale_end_dt2), 'YYYY/MM/DD') AS sale_end_dt2,
                   a.costprice1 AS costprice1,
                   a.saleprice1 AS saleprice1,
                   a.costprice2 AS costprice2,
                   a.saleprice2 AS saleprice2,
                   a.prod_cd AS prod_cd,
                   (CASE WHEN
                        z.tax_type = '2'
                        THEN (SELECT z.saleprice_ex)
                        ELSE (SELECT z.saleprice)
                   END) AS saleprice,
                   z.head_costprice,
                   (CASE WHEN
                        z.prod_nm = ''
                        THEN (SELECT z.prod_kn)
                        ELSE (SELECT z.prod_nm)
                    END) AS prod_nm, 
                   v.abbreviated_name

                   FROM mst1303 a
                   JOIN mst1302 b ON  a.sale_plan_cd = b.sale_plan_cd AND a.organization_id = b.organization_id
                   JOIN mst0201 z ON a.prod_cd = z.prod_cd AND a.organization_id = z.organization_id
                   JOIN v_organization v ON a.organization_id = v.organization_id
                   AND a.sale_str_dt1 != ''
                   ";
                    $sql .= $where;
                    $sql .= " ORDER BY a.prod_cd asc, v.abbreviated_name  ";

                    $Log->trace("END creatSQL");
                    return $sql;
                }
